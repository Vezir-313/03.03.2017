var f = document.getElementsByClassName("slider")[0];

function effectBlur(){
	f.style.filter="blur(2px)";
}

function effectBrightness(){
	f.style.filter="brightness(150%)";
}

function effectContrast(){
	f.style.filter="contrast(180%)";
}

function effectGrayscale(){
	f.style.filter="grayscale(200%)";
}


function effectInvert(){
	f.style.filter="invert(70%)";
}

function effectOpacity(){
	f.style.filter="opacity(40%)";
}